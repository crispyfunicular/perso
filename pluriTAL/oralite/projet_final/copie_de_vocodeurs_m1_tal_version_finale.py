# ---- Synthèse ----
def synthetiser(sequence):
    f0_tot, sp_tot, ap_tot = [], [], []
    for p in sequence:
        dur = dico[p]["dur"]
        f0_val = dico[p]["f0"]

        #On calcule combien de frames correspond à la durée moyenne du phonème.
        #Par exemple si dur = 0.08s et PAS = 0.005s, alors n = 16 frames.
        #Le max(1, ...) garantit qu'on a au moins 1 frame.
        n = max(1, int(dur / PAS))

        #[dico[p]["f0"]] * n crée une liste de n fois la même valeur de F0
        # .extend() ajoute ces valeurs à la liste globale.
        #extend ajoute les éléments d’une liste un par un, alors que append ajoute
        #la liste entière comme un seul bloc.
        if f0_val > 0:
          f0_tot.extend([f0_val] * n)
        else:
          f0_tot.extend([0] * n)

        frames_sp = dico[p]["sp_frames"]
        frames_ap = dico[p]["ap_frames"]

        # prend un bloc contigu de n frames (ou boucle si trop court)
        if len(frames_sp) >= n:
            start = np.random.randint(0, len(frames_sp) - n + 1)
            sp_seg = frames_sp[start:start+n]
            ap_seg = frames_ap[start:start+n]
        else:
            reps = int(np.ceil(n / len(frames_sp)))
            sp_seg = np.tile(frames_sp, (reps, 1))[:n]
            ap_seg = np.tile(frames_ap, (reps, 1))[:n]
        sp_tot.extend(sp_seg)
        ap_tot.extend(ap_seg)

    # WORLD attend des arrays, pas des listes Python
    return pw.synthesize(np.array(f0_tot), np.array(sp_tot), np.array(ap_tot), SR)


# ---- Synthèse diphones ----
def synthetiser_diphones(sequence, seed=None, overlap=3):
    """
    Synthèse concaténative à base de diphones (phones IPA).
    - sequence: liste de phones IPA (ex: ["s","ə","l","y",...])
    - overlap: fondu en frames entre unités
    Backoff: si un diphone manque, on retombe sur le monophone `q`.
    """
    rng = np.random.default_rng(seed)

    if len(sequence) < 2:
        return synthetiser(sequence)

    # nb_bins WORLD (souvent 513)
    any_phone = next(iter(dico))
    nb_bins = int(np.asarray(dico[any_phone]["sp_frames"]).shape[1])

    f0_out = np.zeros((0,), dtype=np.float64)
    sp_out = np.zeros((0, nb_bins), dtype=np.float64)
    ap_out = np.zeros((0, nb_bins), dtype=np.float64)

    def _append_unit(f0_u, sp_u, ap_u):
        nonlocal f0_out, sp_out, ap_out
        f0_u = np.asarray(f0_u).astype(np.float64)
        sp_u = np.asarray(sp_u).astype(np.float64)
        ap_u = np.asarray(ap_u).astype(np.float64)

        if f0_out.shape[0] == 0:
            f0_out = f0_u
            sp_out = sp_u
            ap_out = ap_u
            return

        k = int(overlap)
        k = max(0, min(k, f0_out.shape[0], f0_u.shape[0]))
        if k == 0:
            f0_out = np.concatenate([f0_out, f0_u], axis=0)
            sp_out = np.concatenate([sp_out, sp_u], axis=0)
            ap_out = np.concatenate([ap_out, ap_u], axis=0)
            return

        w = np.linspace(0.0, 1.0, k, dtype=np.float64)
        w2 = w[:, None]

        f0_out[-k:] = (1 - w) * f0_out[-k:] + w * f0_u[:k]
        sp_out[-k:] = (1 - w2) * sp_out[-k:] + w2 * sp_u[:k]
        ap_out[-k:] = (1 - w2) * ap_out[-k:] + w2 * ap_u[:k]

        f0_out = np.concatenate([f0_out, f0_u[k:]], axis=0)
        sp_out = np.concatenate([sp_out, sp_u[k:]], axis=0)
        ap_out = np.concatenate([ap_out, ap_u[k:]], axis=0)

    for p, q in zip(sequence, sequence[1:]):
        key = f"{p}__{q}"
        if key in dico_diph_sp and len(dico_diph_sp[key]) > 0:
            j = int(rng.integers(0, len(dico_diph_sp[key])))
            _append_unit(dico_diph_f0[key][j], dico_diph_sp[key][j], dico_diph_ap[key][j])
        else:
            # Backoff monophone: prendre DIPHONE_LEN_FRAMES frames dans la banque de q
            if q not in dico:
                continue
            sp_frames = np.asarray(dico[q]["sp_frames"])
            ap_frames = np.asarray(dico[q]["ap_frames"])
            n = int(DIPHONE_LEN_FRAMES)
            n_avail = int(sp_frames.shape[0])
            if n_avail <= 0:
                continue
            if n_avail >= n:
                start = int(rng.integers(0, n_avail - n + 1))
                sp_u = sp_frames[start:start + n]
                ap_u = ap_frames[start:start + n]
            else:
                reps = int(np.ceil(n / n_avail))
                sp_u = np.tile(sp_frames, (reps, 1))[:n]
                ap_u = np.tile(ap_frames, (reps, 1))[:n]

            f0_val = float(dico[q]["f0"])
            if np.isnan(f0_val) or f0_val <= 0:
                f0_u = np.zeros(n, dtype=np.float64)
            else:
                f0_u = np.full(n, f0_val, dtype=np.float64)
            _append_unit(f0_u, sp_u, ap_u)

    return pw.synthesize(f0_out, sp_out, ap_out, SR)

