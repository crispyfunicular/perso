def synthetiser(sequence, durees=None, f0s=None):
    f0_tot, sp_tot, ap_tot = [], [], []

    for i, p in enumerate(sequence):
        dur = (durees[i] if durees is not None else dico[p]["dur"])
        n = max(1, int(dur / PAS))

        f0_val = (f0s[i] if f0s is not None else dico[p]["f0"])
        if f0_val is None or np.isnan(f0_val) or f0_val <= 0:
            f0_tot.extend([0.0] * n)
        else:
            f0_tot.extend([float(f0_val)] * n)

        # Format attendu: banque de frames WORLD pour chaque phone IPA
        sp_frames = np.asarray(dico[p]["sp_frames"])
        ap_frames = np.asarray(dico[p]["ap_frames"])

        if sp_frames.ndim == 1:
            # Cas rare: on n'a qu'un vecteur -> on répète
            sp_tot.extend([sp_frames] * n)
            ap_tot.extend([ap_frames] * n)
        else:
            n_avail = sp_frames.shape[0]
            if n_avail <= 0:
                continue
            if n_avail >= n:
                start = np.random.randint(0, n_avail - n + 1)
                sp_seg = sp_frames[start:start + n]
                ap_seg = ap_frames[start:start + n]
            else:
                reps = int(np.ceil(n / n_avail))
                sp_seg = np.tile(sp_frames, (reps, 1))[:n]
                ap_seg = np.tile(ap_frames, (reps, 1))[:n]

            sp_tot.extend(sp_seg)
            ap_tot.extend(ap_seg)

    return pw.synthesize(
        np.array(f0_tot),
        np.array(sp_tot),
        np.array(ap_tot),
        SR
    )

