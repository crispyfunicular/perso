import numpy as np
import pyworld as pw
import textgrids
import soundfile as sf
from collections import defaultdict
from pathlib import Path

SR    = 44100
PAS   = 0.005

# Dictionnaires globaux (agrégés sur tous les fichiers)
dico_sp, dico_ap, dico_f0, dico_dur = (
    defaultdict(list),
    defaultdict(list),
    defaultdict(list),
    defaultdict(list),
)

# Diphones (phones IPA): clé "p__q" -> liste d'exemplaires (matrices de frames)
dico_diph_sp = defaultdict(list)
dico_diph_ap = defaultdict(list)
dico_diph_f0 = defaultdict(list)

# Longueur d'un diphone en frames WORLD (PAS=5ms -> 16 frames ~ 80ms)
DIPHONE_LEN_FRAMES = 16
DIPHONE_HALF = DIPHONE_LEN_FRAMES // 2

# Chemins fichiers audio et TextGrid
wavs = sorted(Path("audio").glob("*.wav"))
for wav_path in wavs:
    NOM = wav_path.stem          # ex: "1-1"
    signal, _ = sf.read(str(wav_path))
    tg_path = wav_path.with_suffix(".TextGrid")
    if not tg_path.exists():
        print(f"[WARN] TextGrid manquant, fichier ignoré: {tg_path}")
        continue
    tier = textgrids.TextGrid(str(tg_path))["phones"]

    # ---- Analyse WORLD ----
    signal     = signal.astype(np.float64)
    f0, t      = pw.harvest(signal, SR)
    f0         = pw.stonemask(signal, f0, t, SR)
    sp         = pw.cheaptrick(signal, f0, t, SR)
    ap         = pw.d4c(signal, f0, t, SR)

    segments = []  # liste ordonnée de tuples (phone, fd, ff)
    for a in tier:
        p = a.text.strip()
        # MFA (Kaldi) utilise typiquement "sil" (silence) et "spn" (bruit/parole non analysable)
        if not p or p in {"sil", "spn"}:
            continue

        fd = int(a.xmin / PAS)
        ff = int(a.xmax / PAS)
        if ff <= fd:
            continue

        # Sécurise les slices vides (peut arriver à cause des arrondis temps→trames)
        sp_seg = sp[fd:ff]
        ap_seg = ap[fd:ff]
        f0_seg = f0[fd:ff]
        if sp_seg.shape[0] == 0:
            continue
        #sp[fd:ff] extrait les frames WORLD du phonème courant — c'est une matrice de
        # shape (n_frames, n_bins). La boucle ajoute chaque frame individuellement dans
        # la liste dico_sp[p]. À la fin on aura toutes les frames de toutes les occurrences
        # du phonème p dans la même liste.
        #Pour f0 en revanche, on ne stocke pas chaque frame mais une seule valeur par
        # occurrence : la moyenne de F0 sur le segment. C'est un choix de simplification.

        for frame in sp_seg: dico_sp[p].append(frame)
        for frame in ap_seg: dico_ap[p].append(frame)
        dico_f0[p].append(np.nanmean(f0_seg))
        dico_dur[p].append(a.xmax - a.xmin)

        segments.append((p, fd, ff))

    # ---- Diphones: fin(p) + début(q) ----
    if DIPHONE_HALF > 0:
        for (p, p_fd, p_ff), (q, q_fd, q_ff) in zip(segments, segments[1:]):
            left_start = p_ff - DIPHONE_HALF
            left_end = p_ff
            right_start = q_fd
            right_end = q_fd + DIPHONE_HALF

            if left_start < p_fd:
                continue
            if right_end > q_ff:
                continue
            if left_start < 0 or right_start < 0:
                continue
            if left_end > sp.shape[0] or right_end > sp.shape[0]:
                continue

            sp_left = sp[left_start:left_end]
            ap_left = ap[left_start:left_end]
            f0_left = f0[left_start:left_end]

            sp_right = sp[right_start:right_end]
            ap_right = ap[right_start:right_end]
            f0_right = f0[right_start:right_end]

            if sp_left.shape[0] != DIPHONE_HALF or sp_right.shape[0] != DIPHONE_HALF:
                continue

            key = f"{p}__{q}"
            dico_diph_sp[key].append(np.concatenate([sp_left, sp_right], axis=0))
            dico_diph_ap[key].append(np.concatenate([ap_left, ap_right], axis=0))
            f0_d = np.concatenate([f0_left, f0_right], axis=0)
            f0_d = np.where(np.isnan(f0_d) | (f0_d <= 0), 0.0, f0_d).astype(np.float64)
            dico_diph_f0[key].append(f0_d)


# Dictionnaire final (moyennes globales par phonème)
dico = {}
for p in dico_sp:
    dico[p] = {
      "sp_frames": np.array(dico_sp[p]),   # (Nframes, nb_bins)
      "ap_frames": np.array(dico_ap[p]),
      "f0": np.mean(dico_f0[p]),
      "dur": np.mean(dico_dur[p]),
    }

